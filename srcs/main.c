#include "../includes/cub3D.h"

int	main(int argc, char **argv)
{
	int	fd;
	char	*line;

	if (argc == 2)
	{
		fd = open(argv[1], O_RDONLY);
		line = get_next_line(fd);
		while (line)
		{
			printf("%s", line);
			free(line);
			line = get_next_line(fd);
		}
	}
	ft_putstr("Wrong number of arguments!\n");
}
